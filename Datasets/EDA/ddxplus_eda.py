def format_case_for_training(case, evidence_map):
    """
    Convert DDXPlus case to LLM training format
    """
    # Decode evidences
    evidences_list = parse_evidences(case['EVIDENCES'])
    symptoms = [decode_evidence_code(ev, evidence_map) for ev in evidences_list[:15]]

    # Create patient description
    symptom_text = ", ".join(symptoms[:10])
    if len(symptoms) > 10:
        symptom_text += f", and {len(symptoms) - 10} other symptoms"

    # Format as instruction-following example
    instruction = f"""A {case['AGE']}-year-old {case['SEX']} patient presents with the following:

Symptoms: {symptom_text}

What is the most likely diagnosis?"""

    output = case['PATHOLOGY']

    # Get differential for additional context
    diff_dx = parse_differential(case['DIFFERENTIAL_DIAGNOSIS'])
    if diff_dx:
        differential_text = ", ".join([d[0] for d in diff_dx[:3] if isinstance(d, list)])
        output += f"\n\nDifferential diagnosis to consider: {differential_text}"

    return {
        "instruction": instruction,
        "input": "",
        "output": output,
        "metadata": {
            "age": case['AGE'],
            "sex": case['SEX'],
            "pathology": case['PATHOLOGY']
        }
    }

